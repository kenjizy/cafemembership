    public static boolean loginAcc(String username, String password){
        dbConnector connector = new dbConnector();
        try{
            String query = "SELECT * FROM tbl_user  WHERE u_username = '" + username + "'";
            ResultSet resultSet = connector.getData(query);
         
                
               if(resultSet.next()){
       try{
       String hashPass=resultSet.getString("u_password");
       String rehash=passwordHasher.hashPassword((password));
       
       if(hashPass.equals(rehash)){
           Session ses=Session.getInstance();
            ses.setId(resultSet.getInt("u_id"));
            ses.setName(resultSet.getString("u_name"));
            ses.setUsername(resultSet.getString("u_username"));
            ses.setPassword(resultSet.getString("u_password"));
            ses.setEmail(resultSet.getString("u_email")); 
            ses.settype(resultSet.getString("u_type")); 
            ses.setStatus(resultSet.getString("u_status")); 
            System.out.println(""+ses.getId());
        return true;  
       }else{
           System.out.println("Pssword did not match");
           return false;
       }
       
       }catch(NoSuchAlgorithmException ex){
           System.out.println(""+ex);
       }
      
    }else{
        return false;
    }

      }catch(SQLException ex){
           return false;
      }
        return false;
      
    } 


    
