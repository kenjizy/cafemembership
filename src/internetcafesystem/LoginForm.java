    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
     Session ses=Session.getInstance();
        if(username.getText().isEmpty()||password.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Fields should not be empty");
        }else{

            if(loginAcc(username.getText(),password.getText())){

                if(ses.getStatus().equals("Active")){
                    if(ses.gettype().equals("Admin")){
                        AdminDashboard ads=new AdminDashboard();                      
                        ads.setVisible(true);
                        JOptionPane.showMessageDialog(null,"Login Sucessful");                       
                        this.dispose();
                    }else{
                       userDashboard ud=new userDashboard();
                       ud.setVisible(true);
                        this.dispose();
                        JOptionPane.showMessageDialog(null,"Login Sucessful");
                       
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"Account Pending");
                }

            }else{
                JOptionPane.showMessageDialog(null,"Account not found");
                username.setText("");
                password.setText("");
            }
        }


    }//GEN-LAST:event_loginActionPerformed

